function Combo()
	if target~=nil then
		if os.clock()>timetoAA then
			if (gun==true and ((GetD(target)>600))) or (gun==false and (GetD(target)<=600 )) then
				CastSpellTarget('Q',myHero)
			end
			AttackTarget(target)
		elseif os.clock() > (timerAnimationBegin + timerAnimationSpeed) then
			MoveToXYZ(GetCursorWorldX(),0,GetCursorWorldZ())
		end
		
	else
		if targetlong~=nil then		
			if targetlong.movespeed>=myHero.movespeed and runningAway(targetlong) and WRDY==1 and GetD(targetlong)>800 and CreepBlock(targetlong.x,targetlong.y,targetlong.z) == 0 then
				CastSpellXYZ('W',GetFireahead(targetlong,6.45,24))
			end
		end
		MoveToMouse()
	end
end

function WPoke()

